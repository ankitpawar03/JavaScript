// for in loop in array

const fruits = ["apple", "banana", "orange", "mango"];

for (let index in fruits){
    console.log(index);
}

// this is the way to iterate over an array using for in loop this is same as for loop but it is more easy to use than for loop and it is more readable than for loop

for (let index in fruits){
    console.log(fruits[index]);
}