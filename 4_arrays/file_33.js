// for of loop in array

const fruits = ["apple", "banana", "orange", "mango"];

for (let fruit of fruits){
    console.log(fruit);
}

// this is the way to iterate over an array using for of loop this is same as for loop but it is more easy to use than for loop and it is more readable than for loop

