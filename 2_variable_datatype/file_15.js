//ternary operator

let age = 18;

let drink1 = "milk";
let drink2 = "wine";

let ans = (age >= 18) ? drink2 : drink1;
//here ? is a ternary operator If the condition is true then the value of drink2 will be assigned to ans otherwise the value of drink1 will be assigned to ans
// : is a else part of ternary operator
console.log(ans);