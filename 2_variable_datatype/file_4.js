// declare constant using const keyword

// we use constant keyword to declare a constant values which can not be changed in future for example value of pi is 3.14 which can not be changed in future

const pi = 3.14;
console.log(pi);

// we can not change value of constant as we did in let and var keyword

// example
// pi = 3.15; // this will give us error because we can not change value of constant

console.log(pi*2);
// we can perform operation with constant pi but we can not change its value