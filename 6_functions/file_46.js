// arrow functions 
//  arrow functions are introduced in ES6 and they are also called as fat arrow functions and they are used to write functions in a shorter way and they are anonymous functions and they are assigned to variables and they are passed as arguments to other functions and they are returned from other functions as well


const add2 = (a, b) => {
    return a + b;
}

console.log(add2(2, 9));

// or

const add3 = (a, b) => a + b;

console.log(add3(2, 100));

